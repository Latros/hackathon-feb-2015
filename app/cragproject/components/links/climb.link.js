var ClimbLink = React.createClass({displayName: "ClimbLink",

  render: function() {
    return (
      React.createElement("span", null, React.createElement("a", {href: "/#/climb/" + this.props.climb.slug, title: this.props.climb.name + " - " + this.props.climb.grade + " at " + this.props.climb.location.name + " on RockClimbCanada.com"}, this.props.climb.name, " - ", this.props.climb.grade), " at ", React.createElement("a", {href: "/#/location/" + this.props.climb.location.slug, title: this.props.climb.location.name + " on RockClimbCanada.com"}, this.props.climb.location.name))
    );
  }

});

CragProject.value('ClimbLink', ClimbLink);